package com.james.hive.utils.entity;

public class ColsInfo {
    private String colName;
    private String colType;
    private String colComent;

    public String getColName() {
        return colName;
    }

    public void setColName(String input) {
        this.colName = input;
    }

    public String getColType() {
        return colType;
    }

    public void setColType(String input) {
        this.colType = input;
    }

    public String getColComent() {
        return colComent;
    }

    public void setColComent(String input) {
        this.colComent = input;
    }
}